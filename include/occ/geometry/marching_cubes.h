namespace impl {
template <typename E>
void march_cube(const std::array<float, 8> &values, E &edge_func) {
    using namespace tables;
    uint32_t cube_index = 0;
    for (size_t i = 0; i < 8; i++) {
        if (values[i] <= 0.0) {
            cube_index |= (1 << i);
        }
    }
    const auto triangles = TRIANGLE_CONNECTION[cube_index];

    for (size_t i = 0; i < 5; i++) {
        if (triangles[3 * i] < 0)
            break;
        for (size_t j = 0; j < 3; j++) {
            size_t edge = triangles[3 * i + j];
            edge_func(edge);
        }
    }
}

inline constexpr float get_offset(float a, float b) {
    float delta = b - a;
    if (delta == 0.0)
        return 0.5;
    return -a / delta;
}

template <typename T> T interpolate(T a, T b, float t) {
    return a * (1.0 - t) + b * t;
}

} // namespace impl

struct MarchingCubes {
    size_t size;
    Eigen::MatrixXf layer0, layer1;
    MarchingCubes(size_t s) : size(s), layer0(s, s), layer1(s, s) {}

    template <typename S>
    void extract(const S &source, std::vector<float> &vertices,
                 std::vector<uint32_t> &indices) {
        auto fn = [&vertices](const Eigen::Vector3f &vertex) {
            vertices.push_back(vertex(0));
            vertices.push_back(vertex(1));
            vertices.push_back(vertex(2));
        };

        extract_impl(source, fn, indices);
    }

    template <typename S>
    void extract_with_normals(const S &source, std::vector<float> &vertices,
                              std::vector<uint32_t> &indices,
                              std::vector<float> &normals) {
        auto fn = [&vertices, &source,
                   &normals](const Eigen::Vector3f &vertex) {
            vertices.push_back(vertex[0]);
            vertices.push_back(vertex[1]);
            vertices.push_back(vertex[2]);

	    occ::timing::start(occ::timing::isosurface_normals);
	    if constexpr(!impl::has_fill_normals<S>::value) {
		auto normal = source.normal(vertex(0), vertex(1), vertex(2));
		normals.push_back(normal[0]);
		normals.push_back(normal[1]);
		normals.push_back(normal[2]);
	    }
	    occ::timing::stop(occ::timing::isosurface_normals);
        };

        extract_impl(source, fn, indices);
	    occ::timing::start(occ::timing::isosurface_normals);
	if constexpr(impl::has_fill_normals<S>::value) {
	    source.fill_normals(vertices, normals);
	}
	occ::timing::stop(occ::timing::isosurface_normals);
    }

  private:
    template <typename S, typename E>
    void extract_impl(const S &source, E &extract_fn,
                      std::vector<uint32_t> &indices) {
        using namespace tables;
        using namespace impl;
        const size_t size_less_one = size - 1;
        const float size_inv = 1.0 / size_less_one;

	occ::timing::start(occ::timing::isosurface_function);
	if constexpr(impl::has_fill_layer<S>::value) {
	    source.fill_layer(0.0, layer0);
	}
	else {
	    for (size_t y = 0; y < size; y++) {
		for (size_t x = 0; x < size; x++) {
		    layer0(x, y) = source(x * size_inv, y * size_inv, 0.0);
		}
	    }
	}
	occ::timing::stop(occ::timing::isosurface_function);

        std::array<Eigen::Vector3f, 8> corners{Eigen::Vector3f::Zero()};
        std::array<float, 8> values{0.0f};

        IndexCache index_cache(size);
        uint32_t index = 0;

        for (size_t z = 0; z < size; z++) {
	    occ::timing::start(occ::timing::isosurface_function);
	    if constexpr(impl::has_fill_layer<S>::value) {
		source.fill_layer((z + 1) * size_inv, layer1);
	    }
	    else {
		for (size_t y = 0; y < size; y++) {
		    for (size_t x = 0; x < size; x++) {
			layer1(x, y) =
			    source(x * size_inv, y * size_inv, (z + 1) * size_inv);
		    }
		}
	    }
	    occ::timing::stop(occ::timing::isosurface_function);

            for (size_t y = 0; y < size_less_one; y++) {
                for (size_t x = 0; x < size_less_one; x++) {
                    for (size_t i = 0; i < 8; i++) {
                        const auto corner = CORNERS[i];
                        const auto cx = corner[0], cy = corner[1],
                                   cz = corner[2];
                        corners[i] = {(x + cx) * size_inv, (y + cy) * size_inv,
                                      (z + cz) * size_inv};

                        if (cz == 0) {
                            values[i] = layer0(x + cx, y + cy);
                        } else {
                            values[i] = layer1(x + cx, y + cy);
                        }
                    }

                    auto fn = [&extract_fn, &index_cache, &indices, &index,
                               &corners, &values, &x, &y](size_t edge) {
                        const uint32_t cached_index =
                            index_cache.get(x, y, edge);
                        if (cached_index > 0) {
                            indices.push_back(cached_index);
                        } else {
                            size_t u = EDGE_CONNECTION[edge][0];
                            size_t v = EDGE_CONNECTION[edge][1];

                            index_cache.put(x, y, edge, index);
                            indices.push_back(index);
                            index += 1;

                            float offset = get_offset(values[u], values[v]);
                            Eigen::Vector3f vertex =
                                interpolate(corners[u], corners[v], offset);
                            extract_fn(vertex);
                        }
                    };

                    march_cube(values, fn);
                    index_cache.advance_cell();
                }
                index_cache.advance_row();
            }
            index_cache.advance_layer();

            std::swap(layer0, layer1);
        }
    }
};

